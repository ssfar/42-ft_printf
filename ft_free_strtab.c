#include "libft.h"

char	**ft_free_strtab(char **tab)
{
	size_t	i;

	if (tab)
	{
		i = 0;
		while (tab[i])
		{
			free(tab[i]);
			i++;
		}
		free(tab);
	}
	return (NULL);
}
