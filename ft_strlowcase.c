#include "libft.h"

char	*ft_strlowcase(char *str)
{
	size_t	i;

	if (str)
	{
		i = 0;
		while (str[i])
		{
			if (ft_isupper(str[i]))
				str[i] += 32;
			i++;
		}
	}
	return (str);
}
